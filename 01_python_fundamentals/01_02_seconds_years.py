# to work out how many seconds in a year
# one minute is 60 seconds

second = 60

# one hour is 60 minutes

minute = 60

# one day is 24 hours

hour = 24

# one regular year (other than leap) is 365 days

day = 365

# year defined

year = second * minute * hour * day

