'''

Write the necessary code to display the area and perimeter of a rectangle that has a width of 2.4 and a height of 6.4.

'''

# defining weight of 2.4 and height of 6.4

w = 2.4
h = 6.4

# defining rectangular area which is height x width

a = w * h

# defining rectangular perimeter which is 2 x (height x width)

p = 2 * a

# displaying area & perimeter

print("Area", a, "Perimieter", p)
